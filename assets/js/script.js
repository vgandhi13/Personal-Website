// add event to all nav link
for (let i = 0; i < navigationLinks.length; i++) {
  navigationLinks[i].addEventListener("click", function () {

    for (let i = 0; i < pages.length; i++) {
      if (this.innerHTML.toLowerCase() === pages[i].dataset.page) {
        pages[i].classList.add("active");
        navigationLinks[i].classList.add("active");
        window.scrollTo(0, 0);
      } else {
        pages[i].classList.remove("active");
        navigationLinks[i].classList.remove("active");
      }
    }

  });
}

function setupModal(modalClass, openButtonClass, closeButtonClass) {
  const openModal = document.querySelector(`.${openButtonClass}`);
  const modal = document.querySelector(`.${modalClass}`);
  const closeModal = document.querySelector(`.${closeButtonClass}`);
  const sidebar = document.querySelector('.sidebar');
  const article = document.querySelector('.targetarticle');
  const navbar = document.querySelector('.navbar');

  openModal.addEventListener("click", function () {
    modal.showModal();
    sidebar.style.filter = 'blur(5px) brightness(0.5)';
    article.style.filter = 'blur(5px) brightness(0.5)';
    navbar.style.filter = 'blur(5px) brightness(0.5)';
  });

  closeModal.addEventListener("click", function () {
    modal.close();
    sidebar.style.filter = '';
    article.style.filter = '';
    navbar.style.filter = '';
  });
}

